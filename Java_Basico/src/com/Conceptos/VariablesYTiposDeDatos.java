package com.Conceptos;

public class VariablesYTiposDeDatos {
    public static void main(String[] args) {

        // Tipo Identificador = 30;
        // Tipo Identificador;
        // Identificador = 30;

        // Enteros
        byte number1 = 10;
        short number2 = 20;
        int number3 = 30;
        long number4 = 40;

        // Punto Flotante
        float number5 = 50.0f;
        double number6 = 60.0d;

        // Booleanos
        boolean number7 = true;
        boolean number8 = false;

        // Caracteres
        char number9 = 'A';

        // Cadena de caracteres
        String number10 = "Hola Mundo";
        String number11 = "Hola Mundo";

        // Objetos
        Object number12 = new Object();
        Object number13 = new Object();

        // Array
        int[] number14 = {10, 20, 30, 40};
        int[] number15 = {10, 20, 30, 40};

        // Tipo Envoltorio
        Integer number16 = null;
        Long number17 = 2L;

    }
}
